package graph;
// Membuat obyek vertex(node)
//dimas cia
public class Vertex {

    // Inisialisasi variable label untuk memberikan nama vertex
    private char label;

    // Constructor 
    public Vertex(char label) {
        this.label = label;
    }

    // Getter dan Setter
    public char getLabel() {
        return label;
    }

    public void setLabel(char label) {
        this.label = label;
    }

}
